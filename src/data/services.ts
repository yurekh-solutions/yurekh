// Helper to find a service by slug
export const findServiceBySlug = (slug: string): { service: ServiceItem; category: ServiceCategory } | null => {
  for (const category of serviceCategories) {
    const service = category.services.find((s) => s.slug === slug);
    if (service) return { service, category };
  }
  return null;
};

// Helper to find a category by slug
export const findCategoryBySlug = (slug: string): ServiceCategory | null => {
  return serviceCategories.find((c) => c.slug === slug) || null;
};

// Get all services as a flat list
export const getAllServices = (): (ServiceItem & { categoryTitle: string; categorySlug: string })[] => {
  return serviceCategories.flatMap((cat) =>
    cat.services.map((s) => ({
      ...s,
      categoryTitle: cat.title,
      categorySlug: cat.slug,
    }))
  );
};

